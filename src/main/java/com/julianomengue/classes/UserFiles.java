package com.julianomengue.classes;

public class UserFiles {

	private int fotos;
	private int audios;
	private int docs;
	private int buddies;

	public UserFiles() {
		super();
	}

	public int getFotos() {
		return fotos;
	}

	public void setFotos(int fotos) {
		this.fotos = fotos;
	}

	public int getAudios() {
		return audios;
	}

	public void setAudios(int audios) {
		this.audios = audios;
	}

	public int getDocs() {
		return docs;
	}

	public void setDocs(int docs) {
		this.docs = docs;
	}

	public int getBuddies() {
		return buddies;
	}

	public void setBuddies(int buddies) {
		this.buddies = buddies;
	}

}
